<h2><b> A Fertilizer</b> </h2>
<div class = "fertilizer" >
<img src="./hom1/fert.jpg"  style="width: 25%" >
</div>
<p>Tea leaves contain the three big nutrients termed NPK, i.e. nitrogen, phosphorus, and potassium. Used tea leaves not only increase agricultural yields and offer farmers an economical natural fertilizer but can reduce the heavy metal toxicity
Tea is a good fertiliser for pot plants, The theory Tea contains nutrients and trace minerals that promote healthy growth in plants. The evidence Dried tea leaves contain about 4.4% nitrogen, 0.24% phosphorus and 0.25% potassium</p>

<h2><b> A Dye</b> </h2>
<div class = "dye" >
<img src="./hom1/dye.jpg"  style="width: 25%" >
</div>
<p>Tea dyeing is a great way to give fabrics an old, antiqued look. This process is best on small projects and not recommended for anything large. Tea can leave spots over the fabric and give you an uneven color. For larger projects we recommend using a commercial dye, Dying fabric with tea is a natural, inexpensive and non-toxic way to change the color of your cloth. The tannnins in black tea make a great dye, giving your fabrics a nice patina. The tannins bond well to natural fibers, so you don't need any mordants to assist with this, making dying with tea an easy method.</p>

